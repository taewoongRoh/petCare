package com.petcare.disease;

public class DiseaseDTO {
	
	int did;
	String dname;
	String dsymptom;
	String dprevent;
	String dcure;
	String heffect;
	String category;
	
	
	public int getDid() {
		return did;
	}
	public void setDid(int did) {
		this.did = did;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public String getDsymptom() {
		return dsymptom;
	}
	public void setDsymptom(String dsymptom) {
		this.dsymptom = dsymptom;
	}
	public String getDprevent() {
		return dprevent;
	}
	public void setDprevent(String dprevent) {
		this.dprevent = dprevent;
	}
	public String getDcure() {
		return dcure;
	}
	public void setDcure(String dcure) {
		this.dcure = dcure;
	}
	public String getHeffect() {
		return heffect;
	}
	public void setHeffect(String heffect) {
		this.heffect = heffect;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	
	
	

}
